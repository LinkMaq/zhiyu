import type { DataPreheatTask, TrainingJob, TrainingMetric } from '../types';

function genMetrics(epochs: number, startLoss: number): TrainingMetric[] {
  const metrics: TrainingMetric[] = [];
  for (let e = 1; e <= epochs; e++) {
    for (let step = 1; step <= 4; step++) {
      const t = (e - 1) * 4 + step;
      metrics.push({
        epoch: e, step: t * 50,
        loss: +(startLoss * Math.pow(0.82, t * 0.3) + Math.random() * 0.05).toFixed(4),
        valLoss: +(startLoss * Math.pow(0.84, t * 0.3) + Math.random() * 0.06).toFixed(4),
        accuracy: +(65 + t * 1.8 - Math.random() * 2).toFixed(2),
        learningRate: +(1e-5 * Math.pow(0.95, t)).toExponential(2) as unknown as number,
        gpuUtilization: Math.round(72 + Math.random() * 22),
        timestamp: new Date(Date.now() - (epochs - e) * 3600000).toISOString(),
      });
    }
  }
  return metrics;
}

